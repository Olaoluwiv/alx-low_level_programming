#include <stdio.h>

/**
 * add - add two integer
 * @a: first unit
 * @b: second unit
 * Return: addtion
 */

int add(int a, int b)
{
	return (a + b);

}
/**
 * sub - subtract two integer
 * @a: first unit
 * @b: second unit
 * Return: subtract final result
 */

int sub(int a, int b)
{
	return (a - b);

}
/**
 * div - divide two integer
 * @a: first unit
 * @b: second unit
 * Return: divide
 */

int div(int a, int b)
{
	return (a / b);

}

/**
 * mod - mode of two integer
 * @a: first unit
 * @b: second unit
 * Return: Return answer
 */

int mod(int a, int b)
{
	return (a & b);

}
/**
 * mul - multiply two integer
 * @a: first unit
 * @b: second unit
 * Return: multiply
 */

int mul(int a, int b)
{
	return (a * b);

}

